#import "SJLiveView.h"

static NSString * const kRTMPUrlString = @"rtmp://10.241.90.109:1990/liveApp/";

@interface SJLiveView ()
@property (nonatomic, copy) NSString *roomId;
@end

@implementation SJLiveView

- (instancetype)initWithRoomId:(NSString *)roomId Frame:(CGRect)frame{
    if(self = [super initWithFrame:frame]){
        self.roomId = roomId;
        self.backgroundColor = [UIColor clearColor];
        
        [self requestAccessForVideo];
        [self requestAccessForAudio];
    }
    return self;
}

- (void)dealloc {
    [self stopLive];
}

#pragma mark -- Public Method
- (void)startLive {
    LFLiveStreamInfo *streamInfo = [LFLiveStreamInfo new];
    streamInfo.url = [NSString stringWithFormat:@"%@%@",kRTMPUrlString,self.roomId];
    [self.session startLive:streamInfo];
    [_session setRunning:YES];
}

- (void)stopLive {
    [_session setRunning:NO];
    [self.session stopLive];
}

- (void)requestAccessForVideo{
    __weak typeof(self) _self = self;
    AVAuthorizationStatus status = [AVCaptureDevice authorizationStatusForMediaType:AVMediaTypeVideo];
    switch (status) {
        case AVAuthorizationStatusNotDetermined:{
            // 许可对话没有出现，发起授权许可
            [AVCaptureDevice requestAccessForMediaType:AVMediaTypeVideo completionHandler:^(BOOL granted) {
                if (granted) {
                    dispatch_async(dispatch_get_main_queue(), ^{
                        [_self startLive];
                    });
                }
            }];
            break;
        }
        case AVAuthorizationStatusAuthorized:{
            // 已经开启授权，可继续
            [_self startLive];
            break;
        }
        case AVAuthorizationStatusDenied:
        case AVAuthorizationStatusRestricted:
            // 用户明确地拒绝授权，或者相机设备无法访问
            
            break;
        default:
            break;
    }
}

- (void)requestAccessForAudio{
    AVAuthorizationStatus status = [AVCaptureDevice authorizationStatusForMediaType:AVMediaTypeAudio];
    switch (status) {
        case AVAuthorizationStatusNotDetermined:{
            [AVCaptureDevice requestAccessForMediaType:AVMediaTypeAudio completionHandler:^(BOOL granted) {
            }];
            break;
        }
        case AVAuthorizationStatusAuthorized:{
            break;
        }
        case AVAuthorizationStatusDenied:
        case AVAuthorizationStatusRestricted:
            break;
        default:
            break;
    }
}


